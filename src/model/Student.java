package model;

public class Student {
	private long studId;
	private String name;
	private String surname;
	private String personCode;
	
	public long getStudId() {
		return studId;
	}
	
	public String getName() {
		return name;
	}
	
	public String getSurname() {
		return surname;
	}
	
	public String getPersonCode() {
		return personCode;
	}
	
	
	
	
}
